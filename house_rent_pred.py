import pandas as pd
import numpy as np

# Load dataset
data = pd.read_csv("data.csv")

# 1. Feature Engineering: Extract property type (Flat, House, Villa) from house_type
def extract_property_type(text):
    text = str(text).lower()
    if 'flat' in text:
        return 'Flat'
    elif 'house' in text:
        return 'House'
    elif 'villa' in text:
        return 'Villa'
    else:
        return 'Other'

data['property_type'] = data['house_type'].apply(extract_property_type)
data = data.drop(columns=['house_type'])

# 2. Locality Cleaning: Group rare localities (appearing < 10 times) into 'Other'
locality_counts = data['locality'].value_counts()
rare_localities = locality_counts[locality_counts < 10].index
data['locality'] = data['locality'].apply(lambda x: 'Other' if x in rare_localities else x)

# 3. Target Leakage Prevention: Drop area_rate and rent from features X
X = data.drop(columns=["rent", "area_rate"])
Y = data["rent"]

from sklearn.model_selection import train_test_split
from sklearn.linear_model import LinearRegression, Lasso, Ridge
from sklearn.ensemble import GradientBoostingRegressor
from sklearn.metrics import r2_score
from sklearn.preprocessing import OneHotEncoder, StandardScaler
from sklearn.compose import make_column_transformer
from sklearn.pipeline import make_pipeline

X_train,X_test,Y_train,Y_test=train_test_split(X,Y,test_size=0.2,random_state=2)

print("Train shape:", X_train.shape)
print("Test shape:", X_test.shape)

# Preprocessing pipeline
column_trans = make_column_transformer(
    (OneHotEncoder(sparse_output=False, handle_unknown='ignore'), 
     ["property_type", "locality", "city", "furnishing"]),
    remainder='passthrough'
)

scalar = StandardScaler(with_mean=False)

# Train and evaluate Linear Regression
lr = LinearRegression()
pipe_lr = make_pipeline(column_trans, scalar, lr)
pipe_lr.fit(X_train, Y_train)
y_pred_lr = pipe_lr.predict(X_test)

# Train and evaluate Lasso (increased max_iter to fix convergence warning)
lasso = Lasso(max_iter=10000)
pipe_lasso = make_pipeline(column_trans, scalar, lasso)
pipe_lasso.fit(X_train, Y_train)
y_pred_lasso = pipe_lasso.predict(X_test)

# Train and evaluate Ridge
ridge = Ridge()
pipe_ridge = make_pipeline(column_trans, scalar, ridge)
pipe_ridge.fit(X_train, Y_train)
y_pred_ridge = pipe_ridge.predict(X_test)

# Train and evaluate Gradient Boosting Regressor (resolves low performance issue)
gbr = GradientBoostingRegressor(random_state=2)
pipe_gbr = make_pipeline(column_trans, gbr)
pipe_gbr.fit(X_train, Y_train)
y_pred_gbr = pipe_gbr.predict(X_test)

# Print results
print("Linear Regression R2:", r2_score(Y_test, y_pred_lr))
print("Lasso R2:", r2_score(Y_test, y_pred_lasso))
print("Ridge R2:", r2_score(Y_test, y_pred_ridge))
print("Gradient Boosting R2:", r2_score(Y_test, y_pred_gbr))

# Save models
import pickle
pickle.dump(pipe_ridge, open("ridge_regression.pkl", "wb"))
pickle.dump(pipe_gbr, open("gradient_boosting.pkl", "wb"))